// terminal
void obtenerTamanoTerminal(int &filas, int &columnas)
{
    struct winsize w;
    ioctl(STDOUT_FILENO, TIOCGWINSZ, &w);
    filas = w.ws_row;
    columnas = w.ws_col;
}

// Dibujar fondo azul completo
void fondoAzul()
{
    int filas, columnas;
    obtenerTamanoTerminal(filas, columnas);

    for (int i = 0; i < filas; ++i)
    {
        cout << BG_BLUE;
        for (int j = 0; j < columnas; ++j)
        {
            cout << " ";
        }
    }
}

